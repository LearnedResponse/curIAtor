"""Capture docs/demo.gif from a real Brave-rendered curIAtor gallery.

This is the release replacement for the deterministic storyboard generated by
``scripts/render_demo_gif.py``. It starts a temporary one-app collection, drives the
SQLite feedback ledger through the same states the demo shows, captures browser
screenshots through Brave's DevTools protocol, and assembles a GIF.
"""
from __future__ import annotations

